import os from "os";

// userInfo() - returns an object containing information about the current user

console.log(os.userInfo());

// totalmem() - returns the total amount of memory in the operating system

console.log(os.totalmem());

// freemem() - returns the amount of free memory in the operating system

console.log(os.freemem());

// cpus() - returns an array of objects containing information about the CPUs

console.log(os.cpus());
